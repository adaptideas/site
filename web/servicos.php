		<p class="desc-images"><img src="/images/ciclo.jpg" alt=""/></p>
		<p>Por mais simples que este flow possa parecer ele se mostra poderoso quando da necessidade de se planejar um processo de aplicação ou otimização do uso de processos dentro de ambientes corporativos. Abaixo uma pequena descrição dos passos:</p>
		<ul class="topics">
			<li>
				<p><strong>Visão</strong> – Dependendo do objetivo do projeto (aplicação/otimização) este passo pode incluir tarefas como: apresentação executiva de Scrum para o board, definição de envolvidos no processo de aplicação/otimização, planejamento estratégico, definição de valores e objetivos do time, dentre outros.</p>
			</li>
			<li>
				<p><strong>Cultura</strong> – O que é de clara percepção nestes projetos de aplicação/otimização é que os profissionais da empresa apenas adquirem a motivação (e comprometimento) necessária após serem treinados. Neste passo do flow serão elaborados e aplicados treinamentos e workshops para todos os níveis que estarão envolvidos com o uso de Scrum na empresa, do executivo ao usuário/cliente interno. Tarefas adicionais que facilitam a inserção das práticas de Scrum no dia-a-dia das pessoas também são executadas.</p>
			</li>
			<li>
				<p><strong>Planejamento</strong> – Com Visão definida e entendida e Cultura preparada para ser trabalhada, o time responsável pela aplicação/otimização de Scrum deve agora elaborar o planejamento tático com as ações que serão necessárias para o alcance da Visão.</p>
			</li>
			<li>
				<p><strong>Execução</strong> – Colocando tudo que foi construído aqui dentro dos projetos Scrum da empresa. Neste passo, o papel da AdaptWorks realizando coaching junto aos membros do time de aplicação/otimização, fará com que estes consigam explorar o potencial máximo de cada um dos projetos envolvidos.</p>
			</li>
			<li>
				<p><strong>Inspeção/Adaptação</strong> – Ao final de um timebox pré-determinado serão realizadas retrospectivas para elevar a melhoria contínua dentro do projeto de aplicação/otimização.</p>
			</li>
		</ul>
		
