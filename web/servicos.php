		<h3 id="agile-coach">1. Agile Coach</h3>
		<p>Para empresas que adotaram Scrum mas encontram dificuldades para enraizar esta framework em sua cultura, oferecemos o serviço de coaching, amparados por um quadro de profissionais com vasto know-how no gerenciamento de projetos em empresas de diversos segmentos. Desta forma, ajudamos ScrumMasters, Product Owners e Times a conduzir corretamente um projeto Scrum ao mesmo tempo em que estes incorporam Scrum em sua cultura.</p>
		
		<h4 id="agile-coach-1">Aplicação de Scrum em times de projetos</h4>
		<p>Este serviço tem como finalidade acelerar a adoção de Scrum e é distribuído através de uma combinação de coaching, mentoring, treinamentos e workshops. Esta opção é ideal para empresas que estão iniciando a utilização de Scrum em seus projetos, mas também pode ser adaptada e aplicada em empresas que já estejam trabalhando com Scrum e queiram melhorar a aplicação de suas práticas e valores, bem como elevar o potencial do time.
		Um coach da AdaptWorks irá participar do planejamento e execução de um conjunto de Sprints a ser definido, por isso a facilidade de adaptação do serviço.</p>
		
		<ul class="topics">
			<li>
				<h5>Parte I: Preparação</h5>
				<p>Durante este período o coach da AdaptWorks irá capturar todas as informações relacionadas ao projeto, time e ambiente no qual o trabalho será feito. Serão realizadas reuniões e apresentações de Scrum para nivelamento de conhecimento, bem como definição de toda a estrutura de trabalho a ser seguida.</p>
			</li>
			<li>
				<h5>Parte II: Treinamentos e workshops</h5>
				<p>Caso haja a necessidade de aplicação de treinamentos e workshops para o time, isto deverá ser feito antes da Sprint Planning Meeting. Certified ScrumMaster, Certified Scrum Product Owner, Introdução aos Métodos Ágeis, Scrum para times e User Stories são alguns dos treinamentos que podem ser aplicados.</p>
			</li>
			<li>
				<h5>Parte III: Sprint Planning Meeting</h5>
				<p>Nesta sessão, que será facilitada por um coach da AdaptWorks, será fornecida uma abordagem estruturada para que o time determine sua capacidade, negocie a meta da Sprint, priorize seu trabalho, defina e estime tarefas para a iteração e analise riscos. Todo o planejamento estratégico e tático para a Sprint será realizado com a facilitação do nosso profissional.</p>
			</li>
			<li>
				<h5>Parte IV: In-Sprint</h5>
				<p>Durante alguns dias da Sprint é importante a participação do nosso Coach auxiliando o time, ScrumMaster e Product Owner em tarefas como: resolução de impedimentos, refinamento de Product Backlog, garantia do funcionamento do processo, elevação de práticas de engenharia, otimização do auto-gerenciamento do time, facilitação de reuniões e workshops, etc.</p>
			</li>
			<li>
				<h5>Parte V: Inspect and Adapt Meeting</h5>
				<p>As sessões de Review e Retrospective serão facilitadas pelo Coach da AdaptWorks e são nessas sessões que o time apresentará o resultado do trabalho realizado na Sprint. A Review Meeting se apresenta como o lugar ideal para colher feedback, revisar métricas e se estabelecer novas prioridades. Na Retrospectiva o nosso profissional irá guiar o trabalho do time para avaliar suas práticas e, após isso, ter em mãos o necessário para ajudá-los no planejamento da próxima iteração.</p>
			</li>
		</ul>
		
		<h4 id="agile-coach-2">Aplicação de Scrum na empresa</h4>
		<p>Os profissionais da AdaptWorks vêm, já há alguns anos, trabalhando na utilização de processos ágeis – principalmente Scrum, FDD e XP – em diferentes ambientes corporativos. Desde empresas da área financeira até empresas de publicidade, passando por fábricas de software, operadoras de cartão de crédito, empresas .com e outras verticais.</p>
		<p>O que pudemos perceber com estas experiências é que, por mais que não exista receita para o sucesso, alguns passos se mostram inevitáveis para a boa aplicação de processos ágeis em praticamente todos os ambientes, por mais que a forma com que os passos sejam diferentes entre esses cenários, eles acabam sempre tendo que ser dados.</p>
